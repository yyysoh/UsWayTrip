package guestroombed;

public class GuestRoomBedVO {
	private int gr_no;
	private int bed_no;
	private int grb_cnt;
	private String bed_type;
	
	public GuestRoomBedVO() {
		
	}

	public int getGr_no() {
		return gr_no;
	}

	public void setGr_no(int gr_no) {
		this.gr_no = gr_no;
	}

	public int getBed_no() {
		return bed_no;
	}

	public void setBed_no(int bed_no) {
		this.bed_no = bed_no;
	}

	public int getGrb_cnt() {
		return grb_cnt;
	}

	public void setGrb_cnt(int grb_cnt) {
		this.grb_cnt = grb_cnt;
	}

	public String getBed_type() {
		return bed_type;
	}

	public void setBed_type(String bed_type) {
		this.bed_type = bed_type;
	}
	
	
}
