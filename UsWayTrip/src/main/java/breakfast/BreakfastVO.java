package breakfast;

public class BreakfastVO {
	
	private int lodging_no;
	private int breakfast_price;
	
	public BreakfastVO() {
		
	}

	public int getLodging_no() {
		return lodging_no;
	}

	public void setLodging_no(int lodging_no) {
		this.lodging_no = lodging_no;
	}

	public int getBreakfast_price() {
		return breakfast_price;
	}

	public void setBreakfast_price(int breakfast_price) {
		this.breakfast_price = breakfast_price;
	}
	
	
}
