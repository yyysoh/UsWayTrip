package memberpoint;

public class memberPointVO {

	private int member_no;
	private String ra_name;
	private String ra_bank;
	private int ra_num;
	
	public memberPointVO() {
		
	}
	
	public int getMember_no() {
		return member_no;
	}
	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}
	public String getRa_name() {
		return ra_name;
	}
	public void setRa_name(String ra_name) {
		this.ra_name = ra_name;
	}
	public String getRa_bank() {
		return ra_bank;
	}
	public void setRa_bank(String ra_bank) {
		this.ra_bank = ra_bank;
	}
	public int getRa_num() {
		return ra_num;
	}
	public void setRa_num(int ra_num) {
		this.ra_num = ra_num;
	}
	
	
	
}
