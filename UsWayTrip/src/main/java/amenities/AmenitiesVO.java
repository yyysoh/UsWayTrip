package amenities;

public class AmenitiesVO {
	private int amenities_no;
	private String amenities_name;
	private int gr_no;
	
	public AmenitiesVO() {
		
	}

	public int getAmenities_no() {
		return amenities_no;
	}

	public void setAmenities_no(int amenities_no) {
		this.amenities_no = amenities_no;
	}

	public String getAmenities_name() {
		return amenities_name;
	}

	public void setAmenities_name(String amenities_name) {
		this.amenities_name = amenities_name;
	}

	public int getGr_no() {
		return gr_no;
	}

	public void setGr_no(int gr_no) {
		this.gr_no = gr_no;
	}
	
	
}
